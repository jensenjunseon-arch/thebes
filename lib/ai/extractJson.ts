// Sonnet usually obeys "return ONLY JSON", but occasionally wraps the object
// in a ```json fence or adds a trailing sentence. This is the tolerant
// extractor — strict enough to reject garbage, loose enough to survive that.

export function extractJson(raw: string): string {
  const trimmed = raw.trim();
  const fenced = trimmed.match(/```(?:json)?\s*([\s\S]*?)\s*```/);
  if (fenced) return fenced[1].trim();

  const firstBrace = trimmed.indexOf("{");
  const lastBrace = trimmed.lastIndexOf("}");
  if (firstBrace !== -1 && lastBrace > firstBrace) {
    return trimmed.slice(firstBrace, lastBrace + 1);
  }
  return trimmed;
}
